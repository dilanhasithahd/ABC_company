    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        try{
           String sql = "UPDATE tags_db SET tagcode=?, tagname=?, relatedtag=? WHERE id = ?";
           con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/abc_db", "root", "");
           
           pst = (PreparedStatement) con.prepareStatement(sql);
           pst.setString(1, txtTagCode.getText());
           pst.setString(2, txtTagName.getText());
           pst.setString(3, txtRelatedTag.getText());
           pst.setString(4, jLabelTagID.getText());
           
           pst.executeUpdate();
           JOptionPane.showMessageDialog(null, "Updated Successully");
       } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showTableData();
        jLabelTagID.setText("");
        txtTagCode.setText("");
        txtTagName.setText("");
        txtRelatedTag.setText("");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        
        String id = jTable1.getValueAt(row, 0).toString();
        String code = jTable1.getValueAt(row, 1).toString();
        String name = jTable1.getValueAt(row, 2).toString();
        String rid = jTable1.getValueAt(row, 3).toString();
        
        jLabelTagID.setText(id);
        txtTagCode.setText(code);
        txtTagName.setText(name);
        txtRelatedTag.setText(rid);
      
    }//GEN-LAST:event_jTable1MouseClicked


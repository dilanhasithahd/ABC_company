    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       try{
           String sql = "DELETE FROM tags_db WHERE id=?";
           con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/abc_db", "root", "");
           
           pst = (PreparedStatement) con.prepareStatement(sql);
           pst.setString(1, jLabelTagID.getText());
           
           pst.executeUpdate();
           JOptionPane.showMessageDialog(null, "Deleted Successully");
       } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
       showTableData();
       
       jLabelTagID.setText("");
      txtTagCode.setText("");
      txtTagName.setText("");
      txtRelatedTag.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

